#include "./../includes/libft.h"

int	ft_putstr_fd(char *s, int fd)
{
	int	count;

	if (!s)
		return (write(fd, "(null)", 6));
	count = 0;
	while (*s)
		count += write(fd, s++, 1);
	return (count);
}
